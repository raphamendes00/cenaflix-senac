    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Filmes filme = new Filmes();
        FilmesDao dao = new FilmesDao();
        boolean status , limpar;
        int resposta;
        
        filme.setNome(txtNome.getText());
        //Para receber data digitada pelo usu√°rio;
        String dataString = txtData.getText();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Date data = null;
        try{
            data = formato.parse(dataString);
        }catch(ParseException p){
            JOptionPane.showMessageDialog(this, "Formato de data inv√°lido, insira uma data no formato dd/MM/aaaa.");
            return;
        }
        //Foi necess√°rio converter o tipo da vari√°vel data para prosseguir;
        java.sql.Date sqlData = new java.sql.Date(data.getTime());
        filme.setDataDeLanc(sqlData);
        filme.setCategoria(txtCategoria.getText());
        
        dao = new FilmesDao();
        status = dao.conectar();
        
        if(status == false){
            JOptionPane.showMessageDialog(null, "Erro ao conectar ao banco de dados");
        }else{
            resposta = dao.Salvar(filme);
            if(resposta == 1){
                JOptionPane.showInternalMessageDialog(null, "T√≠tulo salvo com sucesso");
                txtNome.setText("");
                txtData.setText("");
                txtCategoria.setText("");
                txtNome.requestFocus();
            }else if(resposta == 1062){
                JOptionPane.showMessageDialog(null, "Esse t√≠tulo j√° foi cadastrado");
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao tentar cadastrar novo t√≠tulo");
            }
            dao.desconectar();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

